import dataclasses
import datetime
from typing import AsyncGenerator, AsyncIterator, Generator, Iterator, Protocol

from . import _export

__all__ = export = _export.Export()  # type: ignore


@export
@dataclasses.dataclass
class Stat:
    """Object containing file stats."""

    #: The file last modification date and time (in UTC).
    modified: datetime.datetime

    #: The file size in bytes.
    size: int


@export
class SupportsReadChunks(Protocol):
    """Protocol for objects that support chunked reading."""

    def read_chunks(self, path: str, chunk_size: int = 65535) -> Generator[bytes, None, None]:
        """Return generator for reading existing file in chunks of given
        maximum size.

        If *path* does not point to an existing file
        :exc:`flatfs.exc.PathNotFoundError` will be raised.

        Closing or expiring the returned generator automatically closes
        underlying file.

        :param path:
            The path to a file to read.

        :param chunk_size:
            The maximum chunk size (in bytes).

            This is the maximum length of a single generated bytes object.
        """
        ...


@export
class SupportsWriteChunks(Protocol):
    """Protocol for object that support writing in chunks."""

    def write_chunks(self, path: str, chunk_gen: Generator[bytes, None, None]) -> Generator[int, None, None]:
        """Create or replace file at given path.

        Returns a generator that consumes a chunk from provided *chunk_gen*,
        writes it to the file, and yields number of bytes written for the
        current chunk.

        Closing or expiring returned generator automatically closes underlying
        file and flushes what was written so far.

        :param path:
            The path to a file.

        :param chunk_gen:
            Generator producing chunks to be written.
        """
        ...


@export
class FlatFsReader(SupportsReadChunks, Protocol):
    """Read-only interface to access files in the flat filesystem."""

    def scan(self) -> Iterator[str]:
        """Return iterator that yields normalized paths to existing files."""
        ...

    def stat(self, path: str) -> Stat:
        """Get stats of a file at given path.

        This can be used to retrieve file size, file modification time etc.

        If the file does not exist, :exc:`flatfs.exc.PathNotFoundError` will be
        raised.

        :param path:
            The path to a file.
        """
        ...

    def exists(self, path: str) -> bool:
        """Check if path points to an existing file.

        :param path:
            The path to a file.
        """
        ...


@export
class FlatFsReaderWriter(FlatFsReader, SupportsWriteChunks, Protocol):
    """Read-write interface to access files in the flat filesystem."""

    def remove(self, path: str):
        """Remove existing file at given path.

        Raises :exc:`flatfs.exc.PathNotFoundError` if there was no file to
        remove at given path.

        :param path:
            The path to a file to remove.
        """
        ...


@export
class SupportsAsyncReadChunks(Protocol):
    """Async variant of the :class:`SupportsReadChunks` protocol."""

    def read_chunks(self, path: str, chunk_size: int = 65535) -> AsyncGenerator[bytes, None]:
        """Return a generator for reading file in chunks of given maximum size.

        This is the async version of :meth:`SupportsReadChunks.read_chunks`
        method.

        :param path:
            The path to a file to read.

        :param chunk_size:
            The maximum chunk size, in bytes.
        """
        ...


@export
class SupportsAsyncWriteChunks(Protocol):
    """Async variant of the :class:`SupportsWriteChunks` protocol."""

    def write_chunks(self, path: str, chunk_gen: AsyncGenerator[bytes, None]) -> AsyncGenerator[int, None]:
        """Generator for creating or overwriting files.

        This is the async version of :meth:`SupportsWriteChunks.write_chunks`
        method.

        :param path:
            The path to a file to create or replace.

        :param chunk_gen:
            Async generator yielding file chunks.
        """
        ...


